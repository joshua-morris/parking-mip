def add_border(grid, border_size):
    return (
        [(len(grid[0]) + border_size * 2) * [1] for _ in range(border_size)]
        + [[1] * border_size + row + [1] * border_size for row in grid]
        + [(len(grid[0]) + border_size * 2) * [1] for _ in range(border_size)]
    )


